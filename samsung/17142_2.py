import sys 
from collections import deque 
def input_data() :
    n,m = map(int,sys.stdin.readline().split())
    lab = [list(map(int,sys.stdin.readline().split())) for _ in range(n)]
    virus = deque()
    len_virus = 0
    empty_cnt = 0 
    for i in range(n) :
        for j in range(n): 
            if lab[i][j] == 2 : 
                virus.append((i,j,0)) 
                len_virus +=1 
            elif lab[i][j] == 0 : 
                empty_cnt+=1 
    return n,m,lab,virus,len_virus,empty_cnt 
        
def make_combination(index,cnt,m,combination,len_virus,temp_save,virus):
    if cnt == m : 
        temp = deque() 
        for temp_s in temp_save : 
            temp.append(temp_s)
        combination.append(temp)
    for i in range(len_virus) :
        if i <= index :
            continue 
        temp_save.append(virus[i])
        make_combination(i,cnt+1,m,combination,len_virus,temp_save,virus)
        temp_save.pop()
    return combination 

def diffusion(vi,time_limit):
    dx = [0,0,-1,1]
    dy = [-1,1,0,0]
    c = [[False for _ in range(n)] for _ in range(n)] 
    cnt = 0 
    while vi : 
        x,y,t = vi.popleft()
        if time_limit <= t : 
            return float("inf")
        c[x][y] = True  
        for px,py in zip(dx,dy): 
            nx,ny = x+px, y+py 
            if 0<=nx<n and 0<=ny<n and not c[nx][ny] and lab[nx][ny] != 1 : 
                if lab[nx][ny] == 0:
                    cnt+=1 
                c[nx][ny] = True 
                vi.append((nx,ny,t+1))
            if cnt == empty_cnt : 
                return  t+1
    return float('inf') 
        

